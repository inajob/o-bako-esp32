function loop()
    if btn(0) == 2 and px > 0 then
        px = px - 1
    end
    if btn(1) == 2 and px < 15 then
        px = px + 1
    end
    if btn(2) == 2 and py > 0 then
        py = py - 1
    end
    if btn(3) == 2 and py < 15 then
        py = py + 1
    end
    
    if btn(5) > 0 then
        digit[py * 16 + px + 1] = 1
    end
    if btn(6) > 0 then
        digit[py * 16 + px + 1] = 0
    end
    
    cls()

    color(255,255,255)
    for i = 0, 15 do
        drawrect(0, i * 16,128,16+1)
        drawrect(i * 16, 0,16+1, 128)
    end
    for y = 0, 16 do
        for x = 0, 16 do
            if digit[y*16+x + 1] == 1 then
                fillrect(x * 8, y * 8, 8, 8)
            end
        end
    end
    color(0,255,0)
    drawrect(px*8, py*8 ,8, 8)
end
